    private void jButtoAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtoAgregarActionPerformed

        // Obtener datos del formulario
        int poliza = Integer.parseInt(jTextFieldPoliza.getText());
        String nombre = jTextFieldNombre.getText();
        double prima = Double.parseDouble(jTextFieldPrima.getText());
        String plan = jTextFieldPlan.getText();

        // Verificar si la póliza ya está ingresada
        for (CSeguro seguro : seguros) {
            if (poliza == seguro.obtenerPoliza()) {
                JOptionPane.showMessageDialog(null, "Ya se encuentra ingresado");
                return;
            }
        }

        // Obtener datos adicionales según el tipo de seguro
        if (jRadioButtonVivienda.isSelected()) {
            String ubicacion = jTextFieldUbicacion.getText();
            double precio = Double.parseDouble(jTextFieldPrecio.getText());
            double porcentaje = Double.parseDouble(jTextFieldPorcentaje.getText());

            // Crear un objeto de tipo CVivienda y almacenarlo en el ArrayList
            seguros.add(new CVivienda(poliza, nombre, prima, plan, ubicacion, precio, porcentaje));
        } else {
            int edad = Integer.parseInt(jTextFieldEdad.getText());
            String salud = jTextFieldSalud.getText();

            // Crear un objeto de tipo CMedico y almacenarlo en el ArrayList
            seguros.add(new CMedico(poliza, nombre, prima, plan, edad, salud));
        }

        // Calcular la cobertura del último seguro agregado
        seguros.get(seguros.size() - 1).calcularCobertura();

    }//GEN-LAST:event_jButtoAgregarActionPerformed
    
