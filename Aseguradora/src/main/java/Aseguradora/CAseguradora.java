    private void jButtonMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarActionPerformed
    StringBuilder cadena = new StringBuilder();

    // Recorrer el ArrayList de seguros
    for (CSeguro seguro : seguros) {
        if (seguro != null) {
            cadena.append("Poliza: ").append(seguro.obtenerPoliza())
                  .append("\nNombre: ").append(seguro.obtenerNombre())
                  .append("\nPrima: ").append(seguro.obtenerPrima())
                  .append("\nPlan: ").append(seguro.obtenerPlanSeguro());

            // Verificar el tipo de seguro y obtener información adicional
            if (seguro instanceof CVivienda) {
                cadena.append("\nUbicacion: ").append(((CVivienda) seguro).obtenerUbicacion())
                      .append("\nPrecio: ").append(((CVivienda) seguro).obtenerPrecio())
                      .append("\nPorcentaje: ").append(((CVivienda) seguro).obtenerPorcentaje());
            } else if (seguro instanceof CMedico) {
                cadena.append("\nEdad: ").append(((CMedico) seguro).obtenerEdad())
                      .append("\nSalud: ").append(((CMedico) seguro).obtenerSalud());
            }

            cadena.append("\nCobertura: ").append(seguro.obtenerCobertura()).append("\n\n");
        }
    }

    // Mostrar el contenido con JOptionPane
    if (cadena.length() > 0) {
        JOptionPane.showMessageDialog(null, cadena.toString(), "Lista de Seguros", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "No hay seguros para mostrar", "Lista de Seguros", JOptionPane.WARNING_MESSAGE);
    }
        
        
    }//GEN-LAST:event_jButtonMostrarActionPerformed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // Obtener el número de póliza del campo de texto
    String polizaTexto = jTextFieldPoliza.getText();

    // Validar si el campo de texto está vacío
    if (polizaTexto.isEmpty()) {
        // Mostrar mensaje de advertencia si no se proporciona la información de búsqueda
        JOptionPane.showMessageDialog(null, "Por favor, ingresa el número de póliza", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;  // Salir del método si no se proporciona la información de búsqueda
    }

    try {
        // Convertir el número de póliza a un entero
        int poliza = Integer.parseInt(polizaTexto);

        boolean encontrado = false;
        int pos = -1;

        // Buscar el seguro en el ArrayList
        for (int i = 0; i < seguros.size(); i++) {
            if (poliza == seguros.get(i).obtenerPoliza()) {
                encontrado = true;
                pos = i;
                break;
            }
        }

        if (encontrado) {
            // Obtener la información del seguro encontrado
            String mensaje = "Poliza: " + seguros.get(pos).obtenerPoliza() +
                    "\nNombre: " + seguros.get(pos).obtenerNombre() +
                    "\nPrima: " + seguros.get(pos).obtenerPrima() +
                    "\nPlanSeguro: " + seguros.get(pos).obtenerPlanSeguro() +
                    "\nCobertura: " + seguros.get(pos).obtenerCobertura();

            // Mostrar el mensaje con JOptionPane
            JOptionPane.showMessageDialog(null, mensaje, "Resultado de la búsqueda", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Mostrar mensaje si no se encuentra
            JOptionPane.showMessageDialog(null, "No se encuentra", "Resultado de la búsqueda", JOptionPane.WARNING_MESSAGE);
        }
    } catch (NumberFormatException e) {
        // Manejar la excepción si la entrada no es un número válido
        JOptionPane.showMessageDialog(null, "Por favor, ingresa un número de póliza válido", "Error", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_jButtonBuscarActionPerformed

